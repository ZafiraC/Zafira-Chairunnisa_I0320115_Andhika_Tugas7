# Fungsi Metode Center
str = "Selamat Menunaikan Ibadah Puasa"
s = str.center(50,'^')
print(s)
print(" ")
# Fungsi Metode Replace
str = "Selamat Menunaikan Ibadah Puasa"
print("str: "+str)
print("str replace Menunaikan= ", str.replace("Menunaikan","Menjalankan"))
print(" ")
# Fungsi Metode Capitalize dan Metode Endswith
str = "puasa hari pertama seru dan menyenangkan"
c = str.capitalize()
print(c)
print("Apakah puasa hari pertama menyenangkan?", str.endswith('menyenangkan'))
print(" ")
# Fungsi Metode Upper
str = "semoga puasanya lancar sampai akhir dan semua amal ibadah diterima oleh Allah SWT"
u = str.upper()
print(u)